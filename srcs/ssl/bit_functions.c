#include <stdint.h>

uint32_t	rot_l(uint32_t a, int b)
{
	return (((a) << (b)) | ((a) >> (32 - (b))));
}

uint32_t	rot_r(uint32_t a, int b)
{
	return (((a) >> (b)) | ((a) << (32 - (b))));
}
